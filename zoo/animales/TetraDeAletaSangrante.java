public class TetraDeAletaSangrante {
	
	private String nombre;
	private double tamaño;
	private double pesoGr;
	private int Edadmeses;
	private int cantAletas;
	
	
	
	public void presentarme() {
		System.out.println("Hola me llamo Oliver Atom y soy un Tetra de aleta sangrante");
	}
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public double getTamaño() {
		return tamaño;
	}
	public void setTamaño(double tamaño) {
		this.tamaño = tamaño;
	}
	public double getPesoGr() {
		return pesoGr;
	}
	public void setPesoGr(double pesoGr) {
		this.pesoGr = pesoGr;
	}
	public int getEdadmeses() {
		return Edadmeses;
	}
	public void setEdadmeses(int edadmeses) {
		Edadmeses = edadmeses;
	}
	public int getCantAletas() {
		return cantAletas;
	}
	public void setCantAletas(int cantAletas) {
		this.cantAletas = cantAletas;
	}
	
	
	
}
